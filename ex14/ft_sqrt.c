int	ft_sqrt(int x)
{
	int	i;

	i = 1;
	if (x == 0 || x == 1)
		return (x);
	while (i * i < x && i < 46341)
	{
		i++;
	}
	if (i * i == x)
		return (i);
	return (0);
}
